/* 
 * float_twice - Return bit-level equivalent of expression 2*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_twice(unsigned uf) {
  unsigned exp = (uf&0x7f800000)>>23;
  unsigned sign = uf&(1<<31);
  if(exp==0)  return (uf<<1)|sign;
  if(exp==255)  return uf;
  exp++;
  if(exp==255)  return 0x7f800000|sign;
  return (exp<<23)|(uf&0x807fffff);
}
/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
  if (x == 0)  return 0;
  if (x == 0x80000000)  return 0xcf000000;  //因为要取反，而0x80000000没有对应反，所以手动返回对应的浮点(u)
  int sign = (1 << 31)&x;
  if (sign)  x = - x;
  int E = 0;
  while (x >> E)  E++;  //用2^E来表示x，循环求E
  E--;
  x = x << (31 - E);  //先左移到顶
  int carry = x & 0xff;  //carry记录右移前的末8位进位，右移可能会有进位
  int frac = (x >> 8) & 0x007fffff;  //再右移8位给符号位和阶数位留位置（最前面的1去掉）
  frac += (carry > 128) || ((carry == 128) && (frac & 1));  //进位的条件，大于前一位的一半，或是恰等于一半且为奇数
  if (frac >> 23) {  //当frac为全1时，进位会导致24位变1，此时需要把E加上1
    E++;
    frac = frac & 0x007fffff;
  }
  E += 127;
  return sign | (E << 23) | frac;  //三个部分合并
}
/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(unsigned x) {
  int sign = x >> 31;
  int E = ((x & 0x7f800000) >> 23) - 127;
  int frac = (x & 0x007fffff) | 0x00800000;
  if (E >= 31)  return 0x80000000;
  if (E < 0)  return 0;
  if (E > 23)  frac <<= (E - 23);
  else  frac >>= (23 - E);
  //这里不需要舍入，因为i->f需要用2^E表示一个整数，存在精度问题，而f->i则是用全整数表示2^E，没有精度问题
  if (sign)  return -frac;
  else  return frac;
}

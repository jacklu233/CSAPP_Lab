/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  int tmp1, tmp2, tmp3;
  tmp1 = (~x) & y;
  tmp2 = x & (~y);
  tmp3 = ~((~tmp1)&(~tmp2));
  return tmp3;
}

/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {  //通过符号变化来判定是否大于或小于边界
  int sign = 1<<31;
  int upperBound = ~(sign|0x39);
  int lowerBound = ~0x30;
  upperBound = (sign&(upperBound+x))>>31;
  lowerBound = (sign&(lowerBound+1+x))>>31;
  return !(upperBound|lowerBound);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  x = !!x;
  x = ~x + 1;
  return (x&y)|(~x&z);
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) { 
  //一开始打算只判断y-x的符号，但是没有意识到一个正数减一个负数会溢出
  //所以除了判断y-x还要判断x和y的正负号
  int y_x = (~x+1) + y;
  int signy_x = (y_x>>31) & 1;
  int signXor = ((x ^ y) >> 31) & 1;  //符号异或，相同为0，不同为1
  return ((!signXor)&(!signy_x)) | (signXor&(x >> 31));  //符号相同则看y-x，符号不同则看x的符号
}
